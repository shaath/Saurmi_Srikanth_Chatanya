package javaprogrammes;

public class ForEg {

	public static void main(String[] args) 
	{
		for(int i=1; i <= 10 ; i++)
		{
			System.out.println("Hi");
		}
		System.out.println("*****");
		
		for (int i = 1; i <= 100; i++)
		{
			System.out.println(i);
		}
		System.out.println("*******");
		
		for (int i = 100; i >= 1; i--)
		{
			System.out.println(i);
		}
	}

}
